import java.util.*;
public class Right_Triangle {
    public static void main(String[] args) {
        int n = 5;
        System.out.println("\n--- Pattern 2: Right Triangle ---");
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print("* ");
            }
            System.out.println();
        }

    }
}
