import java.util.*;
public class Squere {

    public static void main(String[] args) {
        int n = 5; 

        System.out.println("--- Pattern 1: Square ---");
        for(int i=0;i<n;i++){
            for(int j=0;j<n;j++){
                System.out.print("* ");
            }
            System.out.println();
        }

    }
}